package quests;

public class QuestCreatureLink {

	private String questEntry;
	private String creatureStart;
	private String creatureEnd;
	
	public QuestCreatureLink() { }

	public String getQuestEntry() {
		return questEntry;
	}

	public void setQuestEntry(String questEntry) {
		this.questEntry = questEntry;
	}

	public String getCreatureStart() {
		return creatureStart;
	}

	public void setCreatureStart(String creatureStart) {
		this.creatureStart = creatureStart;
	}

	public String getCreatureEnd() {
		return creatureEnd;
	}

	public void setCreatureEnd(String creatureEnd) {
		this.creatureEnd = creatureEnd;
	}
	
	
}
